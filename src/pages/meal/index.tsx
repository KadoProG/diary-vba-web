import MealItem from "@/components/mealItems/mealItem";
import { MealRegisteredDate } from "@/interfaces";
import Head from "next/head";
import Link from "next/link";
import styled, { createGlobalStyle } from "styled-components";
import { GetServerSideProps, NextPage } from "next";
import json from "./test.json";
import { useState } from "react";

const DivComponent = styled.div`
  /* width: 767px; */
  width: 325px;
  max-width: 100vw;
  border-bottom: 1px solid black;
`;

const GlobalStyle = createGlobalStyle`
  * {
    margin: 0;
    padding: 0;
    font-family: sans-serif;
    box-sizing: border-box;
    a {
        /* color: $base-color; */
    }
  }
`;

const ButtonUpdate = styled.button`
  margin: 5px;
  border: 1px solid black;
  background: white;
  font-size: 14px;
  padding: 4px;
  cursor: pointer;
`;
type Props = {
  data: MealRegisteredDate[];
};

// JSONデータを取得（現在は静的なファイル）
const jsonBaseData = json;
const fetchJsonData = async (): Promise<MealRegisteredDate[]> => {
  const res = await jsonBaseData.data;
  return res;
};

const Home: NextPage<Props> = ({ data }) => {
  const [data_arr, setData_arr] = useState(data);

  const onBtnUpdateClick = async () => {
    const newData_arr = await fetchJsonData();
    setData_arr(newData_arr);
  };

  return (
    <>
      <GlobalStyle />
      <Head>
        <title>食事メモ</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>
      <h1>食事メモ</h1>
      <DivComponent>
        {data_arr.map((data) => {
          return <MealItem data={data} key={data.id} />;
        })}
      </DivComponent>
      <ButtonUpdate onClick={() => onBtnUpdateClick()}>更新</ButtonUpdate>
      <Link href="/">
        <p>戻る</p>
      </Link>
    </>
  );
};

export default Home;

export const getServerSideProps: GetServerSideProps<Props> = async () => {
  const jsonData = await fetchJsonData();
  return {
    props: {
      data: jsonData,
    },
  };
};
